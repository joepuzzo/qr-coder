const supported = new Set(getSupportedCountries());

/** Countries that appear in the leading dropdown (must exist in intl-phone). */
export const PHONE_ENABLED_COUNTRIES = PHONE_ISO_COUNTRIES.filter((c) =>
  supported.has(c),
);

const enabledSet = new Set(PHONE_ENABLED_COUNTRIES);

export function isPhoneCountryEnabled(iso) {
  return enabledSet.has(iso);
}
